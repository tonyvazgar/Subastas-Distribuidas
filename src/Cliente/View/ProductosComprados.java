    private void actualizarProductos() {
        try {
            productos.setRowHeight(30);
            if (Cliente.Cliente.usuario.getProductosComprados().isEmpty()) {
                return;
            }
            productos.removeAll();
            DefaultTableModel model = (DefaultTableModel) productos.getModel();
            
            for (ProductoInt p: Cliente.Cliente.usuario.getProductosComprados()) {
                model.addRow(new Object [] {p.getNombre(), p.getPrecio(), p.getDescripcion(), p.getDueno().getNombre()});
                
            }
        } catch (RemoteException ex) {
            Logger.getLogger(ProductosComprados.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
     
        
        
    }
